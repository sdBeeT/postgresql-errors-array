    /**
     * @param string $code
     * @return string
     */
    public static function getMessage($code)
    {
        $array = array_flip(self::$errors);
        if (!in_array($code, $array)) {
            $code = 'UNKNOWN';
        }
        return array_search($code, array_flip(self::$errors));
    }
